import java.util.*;
public class incrementanddecrement{
    public static void main(String args[]){
        //post increment
        int a=5;
        System.out.println(a++);
        //pre increment
        int b=7;
        System.out.println(++b);
        //pre decrement
        int c=5;
        System.out.println(++c);
        //post decrement
        int d=6;
        System.out.println(d++);
    }
}
